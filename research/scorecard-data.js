function getRiskRating(rating) {
  var map = {
    'Critical':'Critical','Elevated':'Elevated','High':'High',
    'Moderate':'Moderate','Low':'Low','Very Low':'Very Low'
  };
  return map[rating] || rating;
}

function getRiskColor(rating) {
  if (rating==='Critical')  return '#8B1A1A';
  if (rating==='Elevated')  return '#C0392B';
  if (rating==='High')      return '#D35400';
  if (rating==='Moderate')  return '#B7950B';
  if (rating==='Low')       return '#1D6A3A';
  if (rating==='Very Low')  return '#145A32';
  return '#999';
}

function getScoreColor(score) {
  if (score >= 70) return '#8B1A1A';
  if (score >= 55) return '#C0392B';
  if (score >= 40) return '#D35400';
  if (score >= 25) return '#B7950B';
  return '#1D6A3A';
}

// For CC, score is probability ×100
function getCCColor(prob) {
  if (prob >= 20) return '#8B1A1A';
  if (prob >= 12) return '#C0392B';
  if (prob >= 6)  return '#B7950B';
  if (prob >= 2)  return '#1D6A3A';
  return '#145A32';
}
